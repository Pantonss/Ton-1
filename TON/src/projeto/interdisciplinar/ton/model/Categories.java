package projeto.interdisciplinar.ton.model;

public class Categories
{

    private int    idCategory;
    private String nameCategory;

    public int getIdCategory()
    {
        return idCategory;
    }

    public void setIdCategory(int pIdCategory)
    {
        idCategory = pIdCategory;
    }

    public String getNameCategory()
    {
        return nameCategory;
    }

    public void setNameCategory(String pNameCategory)
    {
        nameCategory = pNameCategory;
    }

}
