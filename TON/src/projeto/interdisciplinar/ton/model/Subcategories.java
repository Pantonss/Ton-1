package projeto.interdisciplinar.ton.model;

public class Subcategories {

	private int idSubcategory;
	private String nameSubcategory;
	private Categories categories;


    public int getIdSubcategory()
    {
        return idSubcategory;
    }
    public void setIdSubcategory(int pIdSubcategory)
    {
        idSubcategory = pIdSubcategory;
    }
    public String getNameSubcategory()
    {
        return nameSubcategory;
    }
    public void setNameSubcategory(String pNameSubcategory)
    {
        nameSubcategory = pNameSubcategory;
    }
    public Categories getCategories()
    {
        return categories;
    }
    public void setCategories(Categories pCategories)
    {
        categories = pCategories;
    }





}
